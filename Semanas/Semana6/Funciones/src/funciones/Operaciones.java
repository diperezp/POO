package funciones;

public class Operaciones {
    
    static double suma(double x, double y){
        return x+y;
    }
    static double suma(double x, double y,double z){
        return x+y+z;
    }
    static double suma(double x, double y,double z,double w){
        return x+y+z+w;
    }    
    
    static double resta(double x, double y){
        return x-y;
    }
    
    static double multiplicacion(double x,double y){
        return x*y;
    }
    static double division(double x, double y){
        return x/y;
    }
    
    
}
