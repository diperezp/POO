package modifiers;


public class ClassStatic {
    
    //Metodos static
    
    static void myStaticMethod(){
        System.out.println("Solo puede ser llamado dentro de la class");    
    }
    
    
    //Metodos public 
    public void myPublicMethod(){
        System.out.println("Puede ser usado por fuera de la class");
        
    }
    
    
    
    
    
    
}
